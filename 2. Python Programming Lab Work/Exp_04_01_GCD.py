# Python Practical 04 - Part A - Compute the GCD of Two Numbers using Math Function.

# import the math function
import math

# taking inputs from user
num_01 = int(input("Enter First Number: "))
num_02 = int(input("Enter Second Number: "))

# using math.gcd funtion from math library
GCD = math.gcd(num_01, num_02)

print("The GCD of", num_01, "and", num_02, "is", GCD, ".")

